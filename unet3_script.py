#Dice Metric

def dice_metric(y_true, y_pred):

    y_true = y_true.cpu().numpy()
    y_pred = y_pred.cpu().numpy()
    
    intersection = np.sum(y_true * y_pred)
    return (2 * intersection) / (np.sum(y_true) + np.sum(y_pred))



